import slugify from "slugify";
import Product from "../models/Product.mjs";
import fs from "fs";

//create product
export const createProduct = async (req, res) => {
  try {
    const { productName, slug, description, price, category, inStock } =
      req.fields;
    const { photo } = req.files;
    //validation
    switch (true) {
      case !productName:
        return res.status(500).send({ error: "Product name is required" });
      case !description:
        return res.status(500).send({ error: "Description is required" });
      case !price:
        return res.status(500).send({ error: "Product price is required" });
      case !category:
        return res.status(500).send({ error: "Product category is required" });
      case !inStock:
        return res.status(500).send({ error: "Product quantity is required" });
      case photo && photo.size > 1000000:
        return res.status(500).send({
          error: "Product photo is required and should be less than 1mb",
        });
    }
    const products = new Product({ ...req.fields, slug: slugify(productName) });

    if (photo) {
      products.photo.data = fs.readFileSync(photo.path);
      products.photo.contentType = photo.type;
    }
    await products.save();
    res.status(201).send({
      success: true,
      message: "product created successfully",
      products,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error in creating product",
    });
  }
};

